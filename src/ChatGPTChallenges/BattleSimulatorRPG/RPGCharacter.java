package ChatGPTChallenges.BattleSimulatorRPG;

public class RPGCharacter {
    public String name;
    public int health, powerAttack, defense, speed;

    public  RPGCharacter (String name, int health, int powerAttack, int defense, int speed){
        this.name = name;
        this.health = health;
        this.powerAttack = powerAttack;
        this.defense = defense;
        this.speed = speed;
    }




}
